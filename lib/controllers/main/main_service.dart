/// هذا الملف مسؤول عن المنطق المساعد الخاص بالشاشة الرئيسية.
///
/// حاليًا الكنترولر الرئيسي بسيط جدًا، لكننا ننشئ هذا الملف
/// للحفاظ على نفس النمط المعماري في المشروع.
///
/// لاحقًا يمكن استخدامه في:
/// - التحقق من التبويب الصحيح
/// - إدارة عدد التبويبات
/// - التعامل مع صلاحيات أو شروط فتح بعض التبويبات
class MainService {
  /// هذه الدالة تتأكد أن رقم التبويب المطلوب صالح.
  ///
  /// إذا خرج الرقم عن النطاق المسموح، نعيد أول تبويب.
  int sanitizeTabIndex({
    required int index,
    required int maxTabs,
  }) {
    if (maxTabs <= 0) return 0;
    if (index < 0) return 0;
    if (index >= maxTabs) return 0;
    return index;
  }
}